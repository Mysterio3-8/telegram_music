export function renderDocs(state) {
  const doc = DOCS[state.docKey] || DOCS.about;
  const body = doc.blocks
    .map(
      ([heading, text]) => `
        <div class="doc-block">
          <div class="doc-block__heading">${heading}</div>
          <div class="doc-block__text">${text}</div>
        </div>
      `
    )
    .join("");

  return `
    <div class="page-head" data-role="page-head">
      <button class="icon-btn" data-action="nav" data-screen="settings" aria-label="Назад">${icon("back")}</button>
      <span>${doc.title}</span>
    </div>
    <div class="doc-body">${body}</div>
  `;
}
